package com.xt.landlords.card.store;

public class CardsBean {
	private String id;
	private String c_center;
	private String c_right;
	private String c_left;
	private String c_under;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getC_center() {
		return c_center;
	}
	public void setC_center(String c_center) {
		this.c_center = c_center;
	}
	public String getC_right() {
		return c_right;
	}
	public void setC_right(String c_right) {
		this.c_right = c_right;
	}
	public String getC_left() {
		return c_left;
	}
	public void setC_left(String c_left) {
		this.c_left = c_left;
	}
	public String getC_under() {
		return c_under;
	}
	public void setC_under(String c_under) {
		this.c_under = c_under;
	}
}
